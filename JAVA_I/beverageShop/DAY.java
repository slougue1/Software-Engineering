/**This class is an enumerated type called DAY. 
 * The valid values are the days of the weeks, MONDAY, TUESDAY, WEDNESDAY,
 *  THURSDAY, FRIDAY, SATURDAY, SUNDAY.
 *  
 * @author Sakina Lougue
 */
public enum DAY 
{
        MONDAY,
        TUESDAY,
        WEDNESDAY, 
        THURSDAY,
        FRIDAY,
        SATURDAY,
        SUNDAY;
}